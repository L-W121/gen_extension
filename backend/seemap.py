def print_map_cell(data, x, y):
    """
    打印 map 中 (x, y) 对应的 cell 信息
    x: 列号 (0 开始)
    y: 行号 (0 开始)
    """
    try:
        cell = data["map"][y][x]
        print(f"坐标 ({x}, {y}) 信息:")
        print(f"  className: {cell['className']}")
        print(f"  text: {cell['text']}")
    except IndexError:
        print("坐标超出 map 范围！")

# 示例使用
print_map_cell(data, 7, 4)
